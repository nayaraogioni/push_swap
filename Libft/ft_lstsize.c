#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	count;

	count = 0;
	while (lst)
	{
		count++;
		lst = lst->next;
	}
	return (count);
}
/* lst e um ponteiro, ou seja, aponta para o primeiro node da lista.
lst = lst->next faz com que o ponteiro lst passe a apontar para o prox node. */
